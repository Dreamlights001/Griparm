#!/bin/bash
# ============================================================
# 预览脚本：观察产线布置、传送带运料、机械臂与相机视角
# ============================================================
# 用途：在正式采集数据前，快速检查场景是否正常
#   - 瑕疵品（anomaly）和正常品（cube）在传送带上循环移动
#   - 机械臂保持在 home 位姿不动
#   - 同时显示 global / wrist 两个相机预览窗口
# ============================================================
#
# 键盘操作：
#   ESC / Q      退出
#   R            重新随机放置物体
#   Space        暂停 / 恢复传送带
#   C            开关相机预览窗口
#   . / ,        加速 / 减速传送带
#   Tab          切换 MuJoCo 主窗口相机
#   鼠标拖拽      旋转/平移/缩放视图
#
# ============================================================

set -euo pipefail
cd "$(dirname "$0")"

echo "[run_preview] 启动产线预览..."

conda run -n sim_env python preview.py \
  --xml env_layout_tuned.xml \
  --width 512 \
  --height 512 \
  --preview-backend auto \
  "$@"
